    private void tblManageAdminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblManageAdminMouseClicked
        DefaultTableModel model = (DefaultTableModel)tblManageAdmin.getModel();
        int row = tblManageAdmin.getSelectedRow();
        
        txtIdAdmin.setText(model.getValueAt(row, 0).toString());
        txtNamaAdmin.setText(model.getValueAt(row, 1).toString());
        txtTglLahir.setText(model.getValueAt(row, 2).toString());
        //buttonGroup1.getSelection().getActionCommand();
        if(model.getValueAt(row, 3).equals("L")){
            rbLakiLaki.setSelected(true);
        }else if (model.getValueAt(row, 3).equals("P")){
            rbPerempuan.setSelected(true);
        }
        txtAlamat.setText(model.getValueAt(row, 4).toString());
        txtTelepon.setText(model.getValueAt(row, 5).toString());
        txtEmail.setText(model.getValueAt(row, 6).toString());
        txtUsernameAdmin.setText(model.getValueAt(row, 7).toString());
        txtPasswordAdmin.setText(model.getValueAt(row, 8).toString());
    }//GEN-LAST:event_tblManageAdminMouseClicked

