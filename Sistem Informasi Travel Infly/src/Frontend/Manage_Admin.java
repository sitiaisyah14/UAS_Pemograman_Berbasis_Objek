    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        DefaultTableModel model = (DefaultTableModel)tblManageAdmin.getModel();
        int row = tblManageAdmin.getSelectedRow();
        
        Admin admin = new Admin().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        admin.delete();
        kosongkanForm();
        JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

