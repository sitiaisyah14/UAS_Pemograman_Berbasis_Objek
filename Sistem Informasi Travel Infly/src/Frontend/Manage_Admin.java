    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String namaAdmin = txtNamaAdmin.getText();
        String tglLahir = txtTglLahir.getText();
        String jenisKelamin = null;
        String alamat = txtAlamat.getText();
        String telepon = txtTelepon.getText();
        String email = txtEmail.getText();
        String usernameAdmin = txtUsernameAdmin.getText();
        String passwordAdmin = txtPasswordAdmin.getText();
        
        if(rbLakiLaki.isSelected()){
            jenisKelamin = "L";
        } else if(rbPerempuan.isSelected()){
            jenisKelamin = "P";
        }
        
        if(!(namaAdmin.isEmpty() && tglLahir.isEmpty() && jenisKelamin.isEmpty() && alamat.isEmpty() 
                && telepon.isEmpty() && email.isEmpty() && usernameAdmin.isEmpty() && passwordAdmin.isEmpty())){
            Admin admin = new Admin();
            admin.setIdadmin(Integer.parseInt(txtIdAdmin.getText()));
            admin.setNamaAdmin(namaAdmin);
            admin.setTglLahir(tglLahir);
            admin.setJenisKelamin(jenisKelamin);
            admin.setAlamatAdmin(alamat);
            admin.setNo_telepon(telepon);
            admin.setEmailAdmin(email);
            admin.setUsernameAdmin(usernameAdmin);
            admin.setPasswordAdmin(passwordAdmin);
            admin.save();
            JOptionPane.showMessageDialog(null, "Penyimpanan data berhasil");
            tampilkanData();
        }
        else{
            JOptionPane.showMessageDialog(null, "Silahkan isi data lengkap");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

