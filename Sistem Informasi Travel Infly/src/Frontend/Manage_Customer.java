    private void tblManageCustomersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblManageCustomersMouseClicked
        DefaultTableModel model = (DefaultTableModel)tblManageCustomers.getModel();
        int row = tblManageCustomers.getSelectedRow();
        
        txtIdUser.setText(model.getValueAt(row, 0).toString());
        txtNamaUser.setText(model.getValueAt(row, 1).toString());
        txtTglLahir.setText(model.getValueAt(row, 2).toString());
        //buttonGroup1.getSelection().getActionCommand();
        if(model.getValueAt(row, 3).equals("L")){
            rbLakiLaki.setSelected(true);
        }else if (model.getValueAt(row, 3).equals("P")){
            rbPerempuan.setSelected(true);
        }
        txtAlamat.setText(model.getValueAt(row, 4).toString());
        txtTelepon.setText(model.getValueAt(row, 5).toString());
        txtEmail.setText(model.getValueAt(row, 6).toString());
        txtUsernameUser.setText(model.getValueAt(row, 7).toString());
        txtPasswordUser.setText(model.getValueAt(row, 8).toString());
    }//GEN-LAST:event_tblManageCustomersMouseClicked

