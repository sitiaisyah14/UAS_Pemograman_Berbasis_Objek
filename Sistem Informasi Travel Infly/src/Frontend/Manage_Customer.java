    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String namaUser = txtNamaUser.getText();
        String tglLahir = txtTglLahir.getText();
        String jenisKelamin = null;
        String alamat = txtAlamat.getText();
        String telepon = txtTelepon.getText();
        String email = txtEmail.getText();
        String usernameUser = txtUsernameUser.getText();
        String passwordUser = txtPasswordUser.getText();
        
        if(rbLakiLaki.isSelected()){
            jenisKelamin = "L";
        } else if(rbPerempuan.isSelected()){
            jenisKelamin = "P";
        }
        
        if(!(namaUser.isEmpty() && tglLahir.isEmpty() && jenisKelamin.isEmpty() && alamat.isEmpty() 
                && telepon.isEmpty() && email.isEmpty() && usernameUser.isEmpty() && passwordUser.isEmpty())){
            User user = new User();
            user.setIduser(Integer.parseInt(txtIdUser.getText()));
            user.setNamaUser(namaUser);
            user.setTglLahir(tglLahir);
            user.setJenisKelamin(jenisKelamin);
            user.setAlamatUser(alamat);
            user.setNo_telepon(telepon);
            user.setEmailUser(email);
            user.setUsernameUser(usernameUser);
            user.setPasswordUser(passwordUser);
            user.save();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            tampilkanData();
        }
        else{
            JOptionPane.showMessageDialog(null, "Silahkan isi data lengkap");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

