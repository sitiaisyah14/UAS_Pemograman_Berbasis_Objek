    public void tampilkanData() {
        Object[] namaKolom = new Object[]{"ID", "Asal", "Tujuan", "Maskapai", "Pesawat", "Harga", "Jam Berangkat", "Jam Kedatangan", "Tanggal Berangkat"};

        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(namaKolom);

        ArrayList<Penerbangan> list = new Penerbangan().getAll();
        Object[] rowData = new Object[9];

        for (Penerbangan bk : list) {
            rowData[0] = bk.getIdpaket();
            rowData[1] = bk.getAsalkota().getNamaKota();
            rowData[2] = bk.getTujuankota().getNamaKota();
            rowData[3] = bk.getPesawat().getNamaPesawat();
            rowData[4] = bk.getMaskapai().getNamaPerusahaan();
            rowData[5] = bk.getHargaTiket();
            rowData[6] = bk.getJamBerangkat();
            rowData[7] = bk.getJamKedatangan();
            rowData[8] = bk.getTanggalBerangkat();

            model.addRow(rowData);
        }
        tblManagePesawat.setModel(model);
    }

    public void cari(String keyword) {
        Object[] namaKolom = new Object[]{"ID", "Asal", "Tujuan", "Maskapai", "Pesawat", "Harga", "Jam Berangkat", "Jam Kedatangan", "Tanggal Berangkat"};

        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(namaKolom);

        ArrayList<Penerbangan> list = new Penerbangan().search(keyword);
        Object[] rowData = new Object[9];

        for (Penerbangan bk : list) {
            rowData[0] = bk.getIdpaket();
            rowData[1] = bk.getAsalkota().getNamaKota();
            rowData[2] = bk.getTujuankota().getNamaKota();
            rowData[3] = bk.getPesawat().getNamaPesawat();
            rowData[4] = bk.getMaskapai().getNamaPerusahaan();
            rowData[5] = bk.getHargaTiket();
            rowData[6] = bk.getJamBerangkat();
            rowData[7] = bk.getJamKedatangan();
            rowData[8] = bk.getTanggalBerangkat();

            model.addRow(rowData);
        }

        tblManagePesawat.setModel(model);
    }

