    private void tblManagePesawatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblManagePesawatMouseClicked
        DefaultTableModel model = (DefaultTableModel) tblManagePesawat.getModel();
        int row = tblManagePesawat.getSelectedRow();

        Penerbangan pn = new Penerbangan();
        pn = pn.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));

        Kota asal = pn.getAsalkota();
        Kota tujuan = pn.getTujuankota();

        txtIdPaket.setText(String.valueOf(pn.getIdpaket()));
        cmbAsal.getModel().setSelectedItem(asal);
        cmbTujuan.getModel().setSelectedItem(tujuan);
        cmbPesawat.getModel().setSelectedItem(pn.getPesawat());
        cmbMaskapai.getModel().setSelectedItem(pn.getMaskapai());
        txtHarga.setText(Integer.toString(pn.getHargaTiket()));
        txtJamBerangkat.setText(pn.getJamBerangkat());
        txtJamKedatangan.setText(pn.getJamKedatangan());
        txtTanggalBerangkat.setText(pn.getTanggalBerangkat());
    }//GEN-LAST:event_tblManagePesawatMouseClicked

