    private void tblManageReservasiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblManageReservasiMouseClicked
        DefaultTableModel model = (DefaultTableModel) tblManageReservasi.getModel();
        int row = tblManageReservasi.getSelectedRow();

        Reservasi rs = new Reservasi();
        rs = rs.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));

        Kota asal = rs.getAsalkota();
        Kota tujuan = rs.getTujuankota();


        txtIdReservasi.setText(String.valueOf(rs.getIdreservasi()));
        cmbUser.getModel().setSelectedItem(rs.getUser());
        cmbAsal.getModel().setSelectedItem(asal);
        cmbTujuan.getModel().setSelectedItem(tujuan);
        cmbPesawat.getModel().setSelectedItem(rs.getPesawat());
        cmbKelas.getModel().setSelectedItem(rs.getKelas());
        txtNamaPemesan.setText(rs.getNamaPemesan());
        txtHarga.setText(rs.getHargaTiket());
        cmbTanggalBerangkat.getModel().setSelectedItem(rs.getPenerbangan());
        txtTanggalBayar.setText(rs.getTglBayar());
        txtStatus.setText(rs.getStatus());
    }//GEN-LAST:event_tblManageReservasiMouseClicked

