    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        

        String namaPemesan = txtNamaPemesan.getText();
        String hargaTiket = txtHarga.getText();
        String tglBayar = txtTanggalBayar.getText();

        if (!(namaPemesan.isEmpty() && hargaTiket.isEmpty())) {
            Reservasi rs = new Reservasi();
            rs.setIdreservasi(Integer.parseInt(txtIdReservasi.getText()));
            rs.setUser((User) cmbUser.getSelectedItem());
            rs.setAsalkota((Kota) cmbAsal.getSelectedItem());
            rs.setTujuankota((Kota) cmbTujuan.getSelectedItem());
            rs.setPesawat((Pesawat) cmbPesawat.getSelectedItem());
            rs.setKelas((String) cmbKelas.getSelectedItem());
            rs.setNamaPemesan(txtNamaPemesan.getText());
            rs.setHargaTiket(txtHarga.getText());
            rs.setPenerbangan((Penerbangan) cmbTanggalBerangkat.getSelectedItem());
            rs.setTglBayar(txtTanggalBayar.getText());
            rs.setStatus(txtStatus.getText());
            rs.save();
            
            tampilkanData();
            JOptionPane.showMessageDialog(null, "Berhasil Disimpan");
        }
        else{
            JOptionPane.showMessageDialog(null, "Silahkan lengkapi data anda");
        }
        
    }//GEN-LAST:event_btnSimpanActionPerformed

